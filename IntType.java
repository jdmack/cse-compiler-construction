class IntType extends NumericType
{
    //----------------------------------------------------------------
    //
    //----------------------------------------------------------------
    public 
    IntType (String strName)
    {
        IntType(strName, 4);
    }

    public
    IntType (String strName, int size)
    {
        super(strName, size);
    }

    //----------------------------------------------------------------
    //
    //----------------------------------------------------------------
    public boolean isBasic()   { return true; }
    public boolean isNumeric() { return true; }
    public boolean isInt()     { return true; }

    //----------------------------------------------------------------
    //    
    //----------------------------------------------------------------
}
