class VarSTO extends STO
{
    //---------------------------------------------------------------------
    //      Constructors
    //---------------------------------------------------------------------
    public VarSTO(STO copySto)
    {
        super(copySto);
    }

    public VarSTO(String strName)
    {
        this(strName, null);
    }

    public VarSTO(String strName, Type type)
    {
        super(strName, type, true);
    }

    //---------------------------------------------------------------------
    //      Methods
    //---------------------------------------------------------------------
    public boolean isVar()
    {
        return true;
    }
}
